function map = locally_authalic_map(v, f, bdyid, bdy)

% Input:
% vertex: nx3 vertex coordinates
% face: mx3 triangulation
% bdy_index : kx1 array of bdy indices
% bdy: target bdy in complex coordinates

% Output:
% map: nx2 coordinates

nv = length(v);

M = authalic_matrix(v,f); 

[mrow,mcol,mval] = find(M(bdyid,:));
M = M - sparse(bdyid(mrow),mcol,mval,nv, nv) + ...
        sparse(bdyid,bdyid,ones(length(bdyid),1),nv, nv);
    
% boundary constraint
c = zeros(nv,1); 
c(bdyid) = bdy;

% solve equation
z = M \ c;
map = [real(z), imag(z)];