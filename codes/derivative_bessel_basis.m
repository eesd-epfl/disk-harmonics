function [dy_Jn, ddy_Jn] = derivative_bessel_basis(m, lmn, rho)
% Returns the first and second derivatives of Bessel functions of the first kind
dy_Jn = 0.5 * lmn .* ( besselj(abs(m-1), lmn .* rho) - besselj(abs(m+1), lmn .* rho) );
ddy_Jn = 0.25 * lmn^2 .* ( besselj(abs(m-2), lmn .* rho) - 2 .* besselj(abs(m), lmn .* rho) + besselj(abs(m+2), lmn .* rho) );
end